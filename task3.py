print('lines and indexing lines')# Строки и индексация строк
example='Топинамбур'
print(example[0])#  Вывел первый символ
print(example[-1])# вывел последний символ
print(example[5:])# вывел вторую половину
print(example[::-1])# Вывел слово наоборот
print(example[1:10:2])# вывел каждый второй символ
print('End')